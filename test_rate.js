function getminMax(array) {
  value = {}
  value['min'] = 100000000;
  value['max'] = 0;

  for (key in array) {
    if (parseFloat(key) > value['max']) {
      value['max'] = parseFloat(key);
    }
    if (parseFloat(key) < value['min']) {
      value['min'] = parseFloat(key);
    }
  }
  return value;

}
